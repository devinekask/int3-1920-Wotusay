  <p>Case woke from a dream of airports, of Molly’s dark leathers moving ahead of him through the concourses of Narita, Schipol, Orly . . . He watched himself buy a 􏰀lat plastic 􏰀lask of Danish vodka at some kiosk, an hour before dawn. Somewhere down in the Sprawl’s ferro-concrete roots, a train drove a column of stale air through a tunnel. The train itself was silent, gliding over its induction cushion, but displaced air made the tunnel sing, bass down into subsonics. Vibration reached the room where he lay and caused dust to rise from the cracks in the desiccated parquet 􏰀loor.</p>
  <p>Opening his eyes, he saw Molly, naked and just out of reach across an expanse of very new pink temper foam. Overhead, sunlight 􏰀iltered through the soot-stained grid of a skylight. One half-meter square of glass had been replaced with chip-board, a fat gray cable emerging there to dangle within a few centimeters of the 􏰀loor. He lay on his side and watched her breathe, her breasts, the sweep of a 􏰀lank de􏰀ined with the functional elegance of a war plane’s fuselage. Her body was spare, neat, the muscles like a dancer’s.</p>
